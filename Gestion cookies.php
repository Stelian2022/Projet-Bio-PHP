 <p>   Je souhaite supprimer les cookies installés sur mon ordinateur</p>

 <p>   Pour supprimer les cookies sur MAC utilisant le navigateur Safari :</p>

 <p>  - Dans Safari, choisissez le menu Édition > Préférences</p>

 <p>  - Cliquez sur « Sécurité » puis  sur « Afficher les cookies »</p>

 <p>   - Sélectionnez les cookies que vous souhaitez supprimer et cliquez sur « Effacer » ou sur « Tout effacer » ; après avoir supprimé les cookies, cliquez sur « Terminé ».</p>

 <p>   Pour supprimer les cookies sur PC :</p>

 <p>   Navigateur Firefox :</p>

 <p>   - Ouvrez le menu et cliquez sur « Historique »</p>

 <p>   - Choisissez « Effacer l'historique récent »</p>

 <p>   - Cochez la case « Cookies » puis cliquez sur « Supprimer »</p>

 <p>Navigateur Internet Explorer :</p>

 <p>   - Dans Internet Explorer, cliquez sur le bouton « Outils », puis sur « Options Internet »</p>

 <p>   - Sous l'onglet Général, sous Historique de navigation, cliquez sur « Supprimer? »</p>

 <p>   - Cochez ensuite « Cookies et données de sites web », puis cliquez sur « Supprimer »</p>

 <p>   - Cliquez sur « OK » pour fermer la fenêtre des « Options Internet »</p>

 <p>   Navigateur Google Chrome :</p>

 <p>   - Cliquez sur l'icône du menu « Personnaliser et paramétrer Google chrome »</p>

 <p>   - Dans le menu, cliquez sur « Plus d'outils », ensuite sur « Effacer les données de navigation? »</p>
 <p>  - Cochez la case « Cookies et autres données de site et de plug- in » et cliquez sur « Effacer les données de navigation »</p>

